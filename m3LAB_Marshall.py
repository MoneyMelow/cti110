score = int( input("What is your test score?"))

if score >= 90:
    letterGrade = "A"
elif score >= 80:
    letterGrade = "B"
elif score >= 70:
    letterGrade = "C"
elif score >= 60:
    letterGrade = "D"
else:
    letterGrade = "F"
    


print("The letter grade is:",letterGrade)
          
    
